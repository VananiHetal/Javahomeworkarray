public class Sum_values {

    //WAP to sum values of an array

    //main method
    public static void main(String[] args) {
        int m1[] = {20, 30, 40, 50, 60, 70}; // int values
        int sum = 0; // declared sum int

        for (int i : m1)
            sum += i;  // condition

        System.out.println("The sum is" + sum);


    }
}




